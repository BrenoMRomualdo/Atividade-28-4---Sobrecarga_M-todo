public class Ex1Impressora {
    
    public void exibir(float a){
        System.out.println(a);
    }

    public void exibir(float a, float b){
       System.out.println(a + ", "+ b);
    }

    public void exibir(float a, String s1){
        System.out.println(a + ", " + s1);
    }

    public void exibir(String s1, float a){
        System.out.println(s1 + ", " + a);
    }

    public void exibir(String s1, String s2, String s3){
        System.out.println(s1 + ", " + s2 + ", " + s3);
    }

    public void exibir(int a, int b, String s1){
        System.out.println(a + ", " + b + ", " + s1);
    }

}   